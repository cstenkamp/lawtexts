prove(K,R,j) :-                     % we can prove yes if
   (kb(K,+,R,_C,j) -> true ;        % we know atrue positive argument or
    argument(+,R,C,D),              % we have an argument for yes
    not(kb(K,+,R,C,_)),             % we do not know it already
    dialog(ask:K:R:C:D,A),          % we ask it ist true
    assert(kb(K,+,R,C,A)),          % now we know it
    A = j                           % and the answer is yes
   ), !,
   not(prove(K,R,n)).               % and no proof for no

prove(K,R,n) :-
   kb(K,+,R,_C,j),           % Geltungsbereich trifft im Prinzip zu
